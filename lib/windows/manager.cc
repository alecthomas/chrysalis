int Manager::go()
/* ===============
	Do it...run the window manager. Called after all windows, etc. have been added using addRegion(). This function keeps going
	until the halt() method is called
*/
{
Region *searchRegion;

	// set region manager's to *this*
	setManager( this );
	if ( root != NULL ) root->setManager( this );

	generateVisibilityMap();
	sendMessage( Region::Redraw );
	sendMessage( Region::Display );

	while ( quitReason == NULL )
	{
		keyboard->update();
		mouse->update();

		// Halt window manager
		if ( keyboard->keyDown( Keyboard::kbALT ) && keyboard->keyDown( Keyboard::kbCTRL ) )
		{
			if ( keyboard->keyDown( Keyboard::kbBACKWARDAPOSTROPHE ) ) halt( 1, "User keyboard halt" );
			if ( keyboard->keyDown( Keyboard::kbR ) ) 
			{
				sendMessage( Region::Redraw );
				sendMessage( Region::Display );
			}
		}

		if ( mouse->eventAvailable() || keyboard->eventAvailable() )
		{
			video->waitRetrace();
			hideMouse();
			// Find out which region that is in focused 
			if ( lockedFocus == NULL )
				if ( ( flags.autoRaise && mouse->moveChange() ) || mouse->buttonPressedSinceLast() || focused == NULL )
					/* Auto-raise, as soon as the mouse moves over the region, it becomes the focuseded window. If auto-raise is not on,
					the region gets focuseded when a button is pressed/released on it. */
					reFocus();
			// Send keyboard messages to appropriate region
			if ( keyboard->eventAvailable() )
			{
				if ( keyboard->keyPress() ) focused->sendMessage( Region::KeyPress, keyboard->getKey() );
				if ( keyboard->keyRelease() ) focused->sendMessage( Region::KeyRelease, keyboard->getKey() );
				keyboard->clearEvent();
			}

			// Send mouse messages to appropriate region
			if ( mouse->moveChange() )
				focused->sendMessage( Region::MouseMove, mouse->getLastX(), mouse->getLastY(), mouse->getX(), mouse->getY() );
			else
				focused->sendMessage( Region::MousePosition, mouse->getX(), mouse->getY() );
			if ( mouse->buttonReleasedSinceLast() ) 
				if ( focused->sendMessage( Region::MouseRelease, mouse->buttonReleasedSinceLast(), mouse->getX(), mouse->getY() ) )
					focused->sendMessage( Region::Display );
			if ( mouse->buttonPressedSinceLast() ) 
				if ( focused->sendMessage( Region::MousePress, mouse->buttonPressedSinceLast(), mouse->getX(), mouse->getY() ) )
					focused->sendMessage( Region::Display );
			showMouse();
		}

		focused->sendMessage( Region::Run );
		if ( focused != root && root != NULL ) root->sendMessage( Region::Run );

		if ( ( searchRegion = removedRegions->reset() ) )
		{
			hideMouse();
			while ( searchRegion != NULL )
			{
				searchRegion->sendMessage( Region::Close );
				regions->remove( searchRegion );
				removedRegions->remove(); 
				generateVisibilityMap();
				sendMessage( Region::PartialDisplay, searchRegion->getAbsX(), searchRegion->getAbsY(), searchRegion->getWidth(), 
					searchRegion->getHeight() );
				delete searchRegion;
				searchRegion = removedRegions->reset();
			}
			reFocus();
			showMouse();
		}
		video->sync();
	}
	clog << "halt (" << quit << "): " << quitReason << endl;
	delete keyboard;
	delete mouse;
	keyboard = NULL;
	mouse = NULL;
}

void Manager::reFocus()
/* =====================
	Find the region that should be in focused
*/
{ 
Region *searchRegion, *lastFocus;

	lastFocus = focused;
	searchRegion = regions->reset_end();
	while ( searchRegion != NULL )
	{
		if ( searchRegion->ping( mouse->getX(), mouse->getY() ) )
		{
			if ( focused == searchRegion ) break; // region is already the focused, don't re-order it
			focused = searchRegion;
			regions->remove( focused );
			regions->insert( regions->highest_priority(), focused );
			break;
		}
		searchRegion = regions->prev();
	}

	// no region is focusedable, make root region the focused...why?
	if ( searchRegion == NULL ) 
		if ( root == NULL )
			focused = this;
		else
			focused = root;
	if ( lastFocus != focused )
	{
		if ( lastFocus != NULL && ( regions->exists( lastFocus ) ) ) lastFocus->sendMessage( Region::UnFocus );
		if ( focused != this ) focused->sendMessage( Region::Focus );
	}
	if ( searchRegion != NULL && searchRegion != lastFocus ) focused->sendMessage( Region::Display );
}

bool Manager::removeRegion( Region *region )
/* ==========================================
	Remove a region
*/
{
	if ( !regions->exists( region ) ) return false;
	removedRegions->insert( 0, region );
	return true;
}

void Manager::halt( int retval, char *reason )
/* ============================================
	Quit the window manager
*/
{
Region *region;

	quit = retval;
	quitReason = reason;
	region = regions->reset();
	while ( region != NULL )
	{
		removeRegion( region );
		region = regions->next();
	}
}

void Manager::showMouse()
/* =======================
	Display cursor
*/
{
	if ( !hideDepth ) return;
	hideDepth--;
	if ( hideDepth ) return;
	cursorBack = page->saveBox( mouse->getX(), mouse->getY(), cursor->width, cursor->height );
	cursor->put( mouse->getX(), mouse->getY(), Page::MASK );
}

void Manager::hideMouse()
/* =======================
	Hide cursor
*/
{
	hideDepth++;
	if ( cursorBack != NULL )
	{
		cursorBack->put( Page::MOV );
		delete cursorBack;
		cursorBack = NULL;
	}
}

bool Manager::lockFocus( Region *region )
/* =======================================
	Lock the focused, so the specified region stays focuseded
*/
{
	if ( lockedFocus ) return false;
	if ( region != focused ) 
	{
		focused->sendMessage( Region::UnFocus );
		region->sendMessage( Region::Focus );
	}
	lockedFocus = focused = region;
	return true;
}

bool Manager::unlockFocus( Region *region )
/* =========================================
	Unlock the focused, so the specified region is no longer locked as the focused
*/
{
	if ( lockedFocus != region ) return false;
	lockedFocus = NULL;
	reFocus();
	return true;
}


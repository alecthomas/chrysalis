bool Manager::display()
/* =====================
	Do a full redraw of the 
*/
{
static int r, g, b;
int l;
ScanLine::Span *span;

	for ( l = 0; l != height; l++ )
	{
		span = scanLines[ l ]->spans->reset();
		while ( span != NULL )
		{
			if ( buffered )
				page->write( buffer->read( span->x, l, span->w ), span->x, l, span->w );
			else
				page->filledBox( span->x, l, span->w, 1, colour );
			span = scanLines[ l ]->spans->next();
		}
	}
}

bool Manager::partialDisplay( int x, int y, int w, int h )
/* =============================================================
	Do a partial redraw of a region
*/
{
int l;
char *i;

	if ( !touching( left, top, width, height, x, y, w, h ) ) return false;
	if ( !clip( i, x, y, w, h, width, height ) ) return false;
	if ( buffered )
		for ( l = y; l != y + h; l++ ) page->write( buffer->read( x, l, w ), x, l, w );
	else
		page->filledBox( x, y, w, h, colour );
	return false;
}

Manager::ScanLine::ScanLine()
{
	spans = new QUEUE <Span> ();
}

Manager::ScanLine::~ScanLine()
{
Span *span;

	span = spans->reset();
	while ( span != NULL )
	{
		delete span;
		span = spans->next();
	}
	delete spans;
}

void Manager::ScanLine::insert( int x, int w )
/* ============================================
	Insert a span into the scan line
*/
{
Span *span, *activeSpan;
int left, right, leftT, rightT;

	left = x; right = x + w;
	span = spans->reset();
	while ( span != NULL )
	{
		leftT = span->x; rightT = span->x + span->w;
		if ( left < rightT && right > leftT )
		{ // span to be inserted touches current span, merge to create one span
			if ( left < leftT ) { leftT = span->x = left; span->w = rightT - leftT; }
			if ( right > rightT ) span->w = right - leftT;

			activeSpan = span;
			span = spans->next();
			while ( span != NULL )
			{
				if ( right >= span->x )
				// touches span
					if ( right >= span->x + span->w ) 
					{ // completely encompasses span
						spans->remove( span );
						delete span;
						span = activeSpan;
					} else
					{ // right edge is in middle 
						activeSpan->w = ( span->x + span->w ) - activeSpan->x;
						spans->remove( span );
						delete span;
						break;
					}
				span = spans->next();
			}
			return;
		}
		span = spans->next();
	}
	spans->insert( x, new Span( x, w ) );
}

void Manager::ScanLine::invert( int width )
/* ==============================
	Invert spans, so that spans represent areas that are NOT covered
*/
{
QUEUE <Span> *newSpans;
Span *span, *newSpan;
int x = 0, l;

	newSpans = new QUEUE <Span> ();
	
	span = spans->reset();
	while ( span != NULL )
	{
		newSpans->insert( x, new Span( x, span->x - x ) );
		x = span->x + span->w;
		spans->remove();
		delete span;
		span = spans->reset();
	}
	newSpans->insert( x, new Span( x, width - x ) );
	spans = newSpans;
}

void Manager::generateVisibilityMap()
/* ============================
	Generate visibility map....map of which areas of root window are visible
*/
{
Region *region;
ScanLine *span;
int l, x, w;

	for ( l = 0; l != height; l++ ) 
	{
		delete scanLines[ l ];
		scanLines[ l ] = new ScanLine();
	}
	region = regions->reset();
	while ( region != NULL )
	{
		for ( l = region->getY(); l != region->getY() + region->getHeight(); l++ )
			if ( !( l < 0 || l >= height || region->getX() >= width || region->getX() + region->getWidth() <= 0 ) ) 
			{
				x = region->getX(); w = region->getWidth();
				if ( x < 0 ) { w += x; x = 0; }
				if ( x + w > width ) w = width - x;
				if ( w <= 0 ) continue;
				scanLines[ l ]->insert( x, w );
			}
		region = regions->next();
	}
	for ( l = 0; l != height; l++ ) scanLines[ l ]->invert( width );
}

bool Manager::setRoot( Region *r )
/* ================================
	Set the root region. Allows weird stuff to occur on the root window
*/
{
	if ( r->getWidth() != getWidth() || r->getHeight() != getHeight() ) return false;
	if ( r->page == NULL ) return false;
	if ( buffered ) delete buffer;
	buffer = r->page;
	buffered = true;
	root = r;
}

